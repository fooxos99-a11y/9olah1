-- Deletes the "سور القرآن" category and its questions, then adds manually curated
-- questions to the "القرآن الكريم" category only.

WITH deleted_surah_questions AS (
  DELETE FROM category_questions
  WHERE category_id IN (
    SELECT id
    FROM categories
    WHERE id = 'a6adb284-f3a9-4718-9f78-a70cd9db49cc'::uuid
  )
  RETURNING id
),
deleted_surah_category AS (
  DELETE FROM categories
  WHERE id = 'a6adb284-f3a9-4718-9f78-a70cd9db49cc'::uuid
  RETURNING id
),
target_category AS (
  SELECT id
  FROM categories
  WHERE id = '0cd4ae69-c73f-45cb-adc3-2ff37c3a6f95'::uuid
  LIMIT 1
),
inserted_rows AS (
  INSERT INTO category_questions (category_id, question, answer, points)
  SELECT id, question, answer, points
  FROM (
    VALUES
      ('ما هي السورة الملقبة بـ "عروس القرآن"؟', 'سورة الرحمن', 200),
