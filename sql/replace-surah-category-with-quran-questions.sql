  CROSS JOIN target_category
  RETURNING question
)
SELECT
  (SELECT COUNT(*) FROM deleted_surah_questions) AS deleted_surah_question_count,
  (SELECT COUNT(*) FROM deleted_surah_category) AS deleted_surah_category_count,
  COUNT(*) AS inserted_quran_question_count
FROM inserted_rows;